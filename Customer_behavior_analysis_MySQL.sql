-- 6. Average purchase : Standard vs Express shipping

SELECT shipping_type,
COUNT(DISTINCT customer_id) AS orders_placed,
ROUND(AVG(purchase_amount),2) AS avg_purchase,
ROUND(SUM(purchase_amount),2) AS total_revenue
FROM customer 
WHERE shipping_type IN('Standard', 'Express')
GROUP BY shipping_type
ORDER BY total_revenue DESC;

-- Business Problem : Unclear if faster shipping leads to higher spending.

-- Impact : Helps optimize shipping pricing strategy
		 -- Encourages premium shipping adoption
         -- Increses average order value 
         
-- 7. Do subscribed customers spend more ? 
-- Compare average speed and total revenue between subscribers and non-subscribers ?

SELECT subscription_status,
COUNT(customer_id) AS users,
ROUND(AVG(purchase_amount),2) AS avg_revenue,
ROUND(SUM(purchase_amount),2) AS total_revenue
FROM customer 
GROUP BY subscription_status
ORDER BY total_revenue DESC;

-- Business Problem : The effectiveness of subscription programs is unknown.

-- Impact : Validates subscription model performance
		 -- Improves customer loyalty programs
         -- Increases customer lifetime value (CLV)

-- 8. Top 5 products with highest discount usage % 

SELECT item_purchased,
COUNT(item_purchased) AS total_number_of_sold,
COUNT(CASE WHEN discount_applied = 'Yes' THEN 1 END) AS number_of_sold_discount_applied,
ROUND(COUNT(CASE WHEN discount_applied = 'Yes' THEN 1 END) * 100.0 / COUNT(*),2) AS discount_percent
FROM customer 
GROUP BY item_purchased
ORDER BY discount_percent DESC LIMIT 5;

-- Business Problem : Some products may be overly dependent on discounts.

-- Impact : Identifies discount-driven products
		 -- Helps optimize pricing strategy
         -- Reduces profit margin loss
         
-- 9. Segment customer into new , returning and loyal based on their total number 
-- of previous purchase and show the count of each segment.

SELECT 
	CASE
		WHEN previous_purchases = '0' THEN 'New Customer'
        WHEN previous_purchases BETWEEN 1 AND 15 THEN 'Returning Customer'
	ELSE 'Loyal Customers'
END AS customer_segment,
COUNT(*) AS customer_count FROM customer 
GROUP BY CASE
		WHEN previous_purchases = '0' THEN 'New Customer'
        WHEN previous_purchases BETWEEN 1 AND 15 THEN 'Returning Customer'
	ELSE 'Loyal Customers'
END;

-- Business Problem : Lack of customer segmentation leads to generic strategies.

-- Impact : Enables personalized marketing
		 -- Improves retention strategies
         -- Increases conversion rates

-- 10. What are the top 3 most purchased products within each category ?

WITH ranked_data AS (
    SELECT 
        category, 
        item_purchased,
        COUNT(item_purchased) AS most_purchased,
        RANK() OVER(
            PARTITION BY category 
            ORDER BY COUNT(item_purchased) DESC
        ) AS RNK
    FROM customer
    GROUP BY category, item_purchased
)
SELECT * FROM ranked_data
WHERE RNK <= 3;

-- Business Problem : The company doesn't know top-performing products within categories.

-- Impact : Improves product placement & reconnendations
		 -- Helps inventory optimazation
         -- Increases sales through best-sellers
         
-- 11. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscribe ?

SELECT 
	CASE
		WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Normal Buyers'
	END AS customer_type,
    subscription_status,
    COUNT(*) AS customer_count,
    ROUND(COUNT(*) * 100.0 / SUM(COUNT(*)) OVER (PARTITION BY CASE
		WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Normal Buyers'
	END),2)
    AS percent
FROM customer 
GROUP BY CASE
		WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Normal Buyers'
	END, subscription_status;
