-- Business Insights Queastion

-- 1. Which products category generate Highest_revenue ?
SELECT category, ROUND(SUM(purchase_amount),2) AS highest_revenue FROM customer
GROUP BY category 
ORDER BY highest_revenue DESC;

-- Business Problem :- Company does not know which category generate highest revenue
-- Impact :- Helps to pripritize high-performing categories
		  -- Optimizes inventory planning
          -- Improves marketing Return on Investment
          
-- 2. Discounts actually increasing purchase value ? 

SELECT discount_applied, 
ROUND(SUM(purchase_amount),2) AS total_revenue,
ROUND(AVG(purchase_amount),2) AS avg_purchase
FROM customer
GROUP BY discount_applied;

-- Buisness Problem : Discounts may reduce profit without increasing sales.
-- Impact :- Identify effectiveness of discounts
		-- Reduce unnecessary discount cost
        -- Improve Profit margins

-- 3. What is the total revenue generated by Gender ? 

SELECT gender, ROUND(SUM(purchase_amount),2) AS Total_revenue FROM customer
GROUP BY gender 
ORDER BY Total_revenue DESC;

-- Business Problem : Lack of understanding of revenue contribution?
-- Impact :- Helps design targated marketing campaign
		  -- Improves customer segmentation stratergy
          -- Enhances personalization efforts
          
-- 4. Which customer used a discount but still spent more than the average purchase amount ?

SELECT customer_id, purchase_amount, discount_applied
FROM customer
WHERE discount_applied = 'Yes' AND 
purchase_amount > (SELECT AVG(purchase_amount) 
FROM customer) LIMIT 10;

-- Business Problem :
-- Company cannot identify high-spending customers who are also diacount users.

-- Impact : Identifies premium discount-sensitive customers
		 -- Enabes targeted discount campaigns
         -- Improves customer retention and revenue

